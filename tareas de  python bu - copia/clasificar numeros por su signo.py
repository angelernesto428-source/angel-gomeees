#CLASIFICADOR DE NUMEROS POR SU SIGNO

print("clasificador de numeros por su signo")

numero = input("ponga el numero : ")

if float(numero) > 0:
    print("el numero es positivo.que bien")

elif float(numero) < 0:
    print("el numero es negativo y malo")

elif float(numero) == 0:
    print("el numero es 0")

else:
    print("esso no es de dios")