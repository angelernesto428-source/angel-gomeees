#COMPARADOR DE NUMEROS
print("comparador de numeros super util")

numero1 = int(input("pon el primer numero:"))
numero2 = int(input("pon el segundo numero:"))

if numero1 > numero2:
   print(f"el número  grande es {numero1} y el {numero2} es el pequeño")

elif numero2 > numero1:
   print(f"el numero grande es {numero2} y el {numero1} es el pequeño")

else:
   print("los numero son iguales")



